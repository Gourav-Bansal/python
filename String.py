# String And String slicing
a = "Gourav is a good boy"
print(len(a))   # length of text
print(a[4])   # printing a particular part
print(a[0:6])   # printing a range
print(a[0:5:2])    # print with leaving gap
print(a[::-1])     # negative index it invert text
print(a[-5:-2])
print(type(a))     # Functions
print(a.isalnum())    # check for alpha numeric
print(a.isalpha())    #  check for alpha
print(a.endswith("boy"))
print(a.count("o"))
print(a.capitalize())
print(a.find("good"))
print(a.upper())